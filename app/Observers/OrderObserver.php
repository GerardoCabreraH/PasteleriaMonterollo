     * Handle the User "creating" event.
     *
     * @param  \App\Models\User  $user
     * @return void
     */
    public function creating(Order $pedido)
    {
        $count = $pedido->whereYear('created_at', '=', now()->year)->count();
        $code = str_pad(++$count, 3, "0", STR_PAD_LEFT);
        $pedido->code = 'P' . $code . date('y');
        $pedido->slug = Str::slug($pedido->code);
    }

